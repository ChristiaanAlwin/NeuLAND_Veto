#include "R3BNeutHt_seg.h"



// ------------------------------------------------------------------------
R3BNeutHt_seg::R3BNeutHt_seg()
  : fX(0.), fY(0.), fZ(0.), fT(0.)
{
}
// ------------------------------------------------------------------------



// ------------------------------------------------------------------------
R3BNeutHt_seg::R3BNeutHt_seg(Double_t x, Double_t y, Double_t z, Double_t t)
{
  fX = x;
  fY = y;
  fZ = z;
  fT = t;
}
// ------------------------------------------------------------------------



// ------------------------------------------------------------------------
R3BNeutHt_seg::~R3BNeutHt_seg()
{
}
// ------------------------------------------------------------------------



// ------------------------------------------------------------------------
Double_t R3BNeutHt_seg::GetP() const
{
  Double_t beta = GetD()/GetT()/29.9792458;
  Double_t gamma = 1./sqrt(1.-beta*beta);
  Double_t p = beta*gamma*1.0086649156*931.494028;
  return p;
}
